// FUNCTION
function multi(n1, n2) {
    const multiplication = n1 * n2
    return multiplication
}
console.log(multi(2,2))

// ARROW FUNCTION
const multiArrow = (n1, n2) => {
    const multiplication = n1 * n2
    return multiplication
}
console.log(multiArrow(2,3))

// ARROW FUNCTION 2
const multiArrow2 = (n1, n2) => n1 * n2
console.log(multiArrow2(2,4))
